public class TestAssign {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int x =10;
        x+=5; //x=x+5
        System.out.println("x="+x);
        x-=5; //x=x-5
        System.out.println("x="+x);
        x*=5; //x=x*
        System.out.println("x="+x);
        x/=10; //x=x/10
        System.out.println("x="+x);
    }
    
}
